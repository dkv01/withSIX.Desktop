namespace Mac.Arma.Config
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Represents a class reference with a Config. </summary>
    /// <remarks>   </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    public abstract class ConfigReference : ConfigEntry
    {
        internal ConfigReference(string name)
            : base(name) {}
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Represents a "delete 'class' entry within a configuration. </summary>
    /// <remarks>  </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    public class DeleteReference : ConfigReference
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        /// <remarks> </remarks>
        /// <param name="name"> The name of the deleted class. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public DeleteReference(string name)
            : base(name) {}
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Represents an external class reference </summary>
    /// <remarks>    </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    public class ExternReference : ConfigReference
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor for a new extern class </summary>
        /// <remarks>   </remarks>
        /// <param name="name"> The name of the class. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public ExternReference(string name)
            : base(name) {}
    }
}